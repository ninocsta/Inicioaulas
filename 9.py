# Crie um algoritmo que leia um número e mostre o seu dobro, o seu triplo e sua raiz quadrada:
n = float(input('Digite um número para receber seu dobro, triplo e sua raiz quadrada: '))
n1 = n * 2
n2 = n * 3
n3 = n ** (1/2)
print(f'Seu número é {n}, o dobro é {n1}, o triplo é {n2} e a raiz quadrada é {n3} ')
